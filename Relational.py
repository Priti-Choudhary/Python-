def relational_operators(a, b):  
    print(f"a = {a}, b = {b}")  

    print("a < b:", a < b)   # Less than  
    print("a <= b:", a <= b)  # Less than or equal to  
    print("a > b:", a > b)   # Greater than  
    print("a >= b:", a >= b)  # Greater than or equal to  
    print("a == b:", a == b)  # Equal to  
    print("a != b:", a != b)  # Not equal to  
